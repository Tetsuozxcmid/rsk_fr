// Р вЂ”Р В°Р С–РЎР‚РЎС“Р В·Р С”Р В° .env Р С”Р В°Р С” fallback (Р Р…Р В° РЎРѓР В»РЎС“РЎвЂЎР В°Р в„– Р ВµРЎРѓР В»Р С‘ Next.js Р Р…Р Вµ Р В·Р В°Р С–РЎР‚РЎС“Р В·Р С‘Р В»)
function loadEnvFallback() {
  ['.env.local', '.env'].forEach((fileName) => {
    const envPath = path.join(process.cwd(), fileName);
    if (!fs.existsSync(envPath)) return;

    try {
      fs.readFileSync(envPath, 'utf-8').split('\n').forEach((line) => {
        const trimmed = line.trim();
        if (!trimmed || trimmed.startsWith('#')) return;
        const eqIndex = trimmed.indexOf('=');
        if (eqIndex === -1) return;
        const key = trimmed.slice(0, eqIndex).trim();
        const val = trimmed.slice(eqIndex + 1).trim();
        if (key && !process.env[key]) {
          process.env[key] = val;
        }
      });
    } catch (e) {
      console.error(`[Analytics] РћС€РёР±РєР° Р·Р°РіСЂСѓР·РєРё ${fileName}:`, e.message);
    }
  });
}
loadEnvFallback();

