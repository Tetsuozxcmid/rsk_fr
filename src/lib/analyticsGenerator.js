// Р СџР В°РЎР‚РЎРѓР С‘Р С Р С•РЎвЂљР Р†Р ВµРЎвЂљ LLM Р Р† РЎРѓР ВµР С”РЎвЂ Р С‘Р С‘
function parseAnalysis(text) {
  const sections = [];
  const lines = text.split('\n');
  let currentTitle = '';
  let currentContent = '';

  for (const line of lines) {
    // Р ВРЎвЂ°Р ВµР С Р В·Р В°Р С–Р С•Р В»Р С•Р Р†Р С”Р С‘ (## Р С‘Р В»Р С‘ **Р В·Р В°Р С–Р С•Р В»Р С•Р Р†Р С•Р С”**)
    const headerMatch = line.match(/^#{1,3}\s+(.+)/) || line.match(/^\*\*(.+)\*\*$/);
    if (headerMatch) {
      if (currentTitle || currentContent.trim()) {
        sections.push({ title: currentTitle, content: currentContent.trim() });
      }
      currentTitle = headerMatch[1].replace(/\*\*/g, '').trim();
      currentContent = '';
    } else {
      // Р Р€Р В±Р С‘РЎР‚Р В°Р ВµР С markdown-РЎвЂћР С•РЎР‚Р СР В°РЎвЂљР С‘РЎР‚Р С•Р Р†Р В°Р Р…Р С‘Р Вµ
      const clean = line.replace(/\*\*/g, '').replace(/\*/g, '').replace(/^[-РІР‚Сћ]\s*/, '  - ');
      currentContent += clean + '\n';
    }
  }
  if (currentTitle || currentContent.trim()) {
    sections.push({ title: currentTitle, content: currentContent.trim() });
  }

  return sections.length > 0 ? sections : [{ title: 'Аналитический отчёт', content: text }];
}

